public class Education {
    
    private String dateFromM;
    private String dateFromY;
    private String dateToM;
    private String dateToY;
    
    private String collegeName;
    private String specialty;
    private String gpa;

    public String getDateFromM() {
        return dateFromM;
    }

    public void setDateFromM(String dateFromM) {
        this.dateFromM = dateFromM;
    }

    public String getDateFromY() {
        return dateFromY;
    }

    public void setDateFromY(String dateFromY) {
        this.dateFromY = dateFromY;
    }

    public String getDateToM() {
        return dateToM;
    }

    public void setDateToM(String dateToM) {
        this.dateToM = dateToM;
    }

    public String getDateToY() {
        return dateToY;
    }

    public void setDateToY(String dateToY) {
        this.dateToY = dateToY;
    }

    public String getCollegeName() {
        return collegeName;
    }

    public void setCollegeName(String collegeName) {
        this.collegeName = collegeName;
    }

    public String getSpecialty() {
        return specialty;
    }

    public void setSpecialty(String specialty) {
        this.specialty = specialty;
    }

    public String getGpa() {
        return gpa;
    }

    public void setGpa(String gpa) {
        this.gpa = gpa;
    }
    
    
    
}

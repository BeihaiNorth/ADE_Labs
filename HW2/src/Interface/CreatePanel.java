    public boolean validDate(String m1, String y1, String m2, String y2)
    {
        int M1 = Integer.parseInt(m1);
        int Y1 = Integer.parseInt(y1);
        int M2 = Integer.parseInt(m2);
        int Y2 = Integer.parseInt(y2);
        
        if ((Y2 > Y1 ||(Y2 == Y1 && M2 > M1))
                && M1 >= 1
                && M1 <= 12
                && Y1 <= 2016
                && Y1 >= 1916
                && M2 >= 1
                && M2 <= 12
                && Y2 >= 1916
                && Y2 <= 2016) {
            return true;
        } else { 
            JOptionPane.showMessageDialog(null, "Date Illegal, Please check.");
            return false;
        }
            
    }

    

    public boolean validphone(String str){
        boolean isNum =false;
        for (int i = str.length();--i>=0;){  
            if (!Character.isDigit(str.charAt(i))){
            isNum = false;
            break;
            }
        isNum = true;
        }
        if (isNum && str.length()==10) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Please fill a valid USA phone number");
            return false;
        }
    }

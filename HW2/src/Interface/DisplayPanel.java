    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int selectedrow = resumeTable.getSelectedRow();
        
        if (selectedrow >= 0) {
            Resume r = (Resume)resumeTable.getValueAt(selectedrow, 0);
            rh.deleteResume(r);
            JOptionPane.showMessageDialog(null, "This resume has been deleted.");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please selected a row!");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed


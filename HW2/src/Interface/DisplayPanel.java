    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)resumeTable.getModel(); 
        dtm.setRowCount(0);
        
        for(Resume resume:rh.getResumeHistory())
        {
            Object row[] = new Object[4];
            row[0] = resume;
            row[1] = resume.getLastName();
            row[2] = resume.getSpecialty();
            row[3] = resume.getGpa();
            dtm.addRow(row);
            
        }
    }

    
